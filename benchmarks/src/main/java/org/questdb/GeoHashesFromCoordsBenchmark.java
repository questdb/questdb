package org.questdb;

import io.questdb.cairo.GeoHashes;
import io.questdb.std.Rnd;
import org.openjdk.jmh.annotations.*;
import org.openjdk.jmh.runner.Runner;
import org.openjdk.jmh.runner.options.OptionsBuilder;

import java.util.concurrent.TimeUnit;

@State(Scope.Thread)
@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.NANOSECONDS)
public class GeoHashesFromCoordsBenchmark {

    static Rnd rnd = new Rnd();

    public static void main(String[] args) throws Exception {
        new Runner(new OptionsBuilder()
                .include(GeoHashesFromCoordsBenchmark.class.getSimpleName())
                .warmupIterations(2)
                .measurementIterations(3)
                .forks(1)
                .build()).run();
    }

    @Benchmark
    public long fromLatLon() {
        double lat = rnd.nextDouble() * 90.0;
        double lon = rnd.nextDouble() * 180.0;
        return GeoHashes.fromCoordinatesDegUnsafe(lat, lon, 60);
    }
}

    /**
     * Negates this Decimal256 in-place using two's complement arithmetic.
     * Changes the sign of the decimal number (positive becomes negative and vice versa).
     */
    public void negateNonNull() {
        ll = ~ll + 1;
        long c = ll == 0L ? 1L : 0L;
        lh = ~lh + c;
        c = (c == 1L && lh == 0L) ? 1L : 0L;
        hl = ~hl + c;
        c = (c == 1L && hl == 0L) ? 1L : 0L;
        hh = ~hh + c;
    }


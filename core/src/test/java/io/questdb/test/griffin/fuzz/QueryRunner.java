package io.questdb.test.griffin.fuzz;

import io.questdb.cairo.CairoEngine;
import io.questdb.cairo.CairoException;
import io.questdb.cairo.CursorPrinter;
import io.questdb.cairo.ImplicitCastException;
import io.questdb.cairo.SqlJitMode;
import io.questdb.cairo.sql.Record;
import io.questdb.cairo.sql.RecordCursor;
import io.questdb.cairo.sql.RecordCursorFactory;
import io.questdb.cairo.sql.RecordMetadata;
import io.questdb.griffin.SqlException;
import io.questdb.griffin.SqlExecutionContext;
import io.questdb.std.NumericException;
import io.questdb.std.ObjList;
import io.questdb.std.str.StringSink;

import java.util.Arrays;
import java.util.regex.Matcher;
import java.util.regex.Pattern;

/**
 * Runs one generated query, materializes every value, and decides whether
 * a thrown exception is acceptable. The oracle is crash-only:
 * <ul>
 *   <li>{@link SqlException} &rarr; accepted (parser, compiler and
 *       type-system errors such as "cannot compare X with Y");</li>
 *   <li>{@link ImplicitCastException} &rarr; accepted (runtime cast
 *       failures for out-of-range literals);</li>
 *   <li>{@link NumericException} &rarr; accepted (parse failures for
 *       numeric strings);</li>
 *   <li>anything else, including any {@link CairoException} &rarr;
 *       reported as a failure. Queries generated by this fuzzer should
 *       never leak a CairoException; if one escapes, the engine is
 *       surfacing an internal error through the query API.</li>
 * </ul>
 * "Materialize every value" means we call a per-column accessor for every
 * row via {@link CursorPrinter#printColumn}, which exercises the type's
 * read path end to end.
 * <p>
 * Differential modes:
 * <ul>
 *   <li>{@code diffJit} runs the same SQL on the primary table twice
 *       &mdash; once with {@link SqlJitMode#JIT_MODE_ENABLED}, once with
 *       {@link SqlJitMode#JIT_MODE_DISABLED} &mdash; and compares the
 *       two materializations. Catches JIT vs. Java-filter divergences.</li>
 *   <li>{@code diffShadow} rewrites the SQL to read from the shadow
 *       sibling (same data, independently random parquet/index settings)
 *       and compares the two materializations at JIT-off. Catches
 *       silent storage divergences across native/parquet partitions and
 *       across indexed/non-indexed symbol columns.</li>
 * </ul>
 * The two modes share a pivot: {@code primary @ JIT-off}. With both
 * enabled, three runs per query are required (primary @ JIT-on,
 * primary @ JIT-off, shadow @ JIT-off); the JIT diff compares the first
 * two, the storage diff compares the second and third. Either divergence
 * fails the query, and the failure message names the axis.
 * <p>
 * Row-set comparison is a multiset (line-sorted) so parallel iteration
 * order does not produce false positives. {@code deterministic=false}
 * queries (LIMIT over parallel GROUP BY / hash join etc.) compare row
 * counts only.
 * <p>
 * Two storage-diff escape valves keep the oracle focused on data
 * correctness rather than planner artefacts:
 * <ul>
 *   <li>If both sides throw the same allowlisted exception class (even
 *       with different messages), the query was rejected by both paths
 *       and treated as skip. Index/parquet differences can change which
 *       sub-expression the compiler reports first.</li>
 *   <li>If exactly one side throws a known planner-sensitivity error
 *       (currently the time-series-join ASC-timestamp check), the diff
 *       is treated as skip. That check inspects cursor metadata that
 *       legitimately differs between an indexed-symbol lookup (loses
 *       TS-ASC) and a full scan (preserves it).</li>
 * </ul>
 */
public final class QueryRunner {
    private final boolean diffJit;
    private final boolean diffShadow;
    private final CairoEngine engine;
    private final SqlExecutionContext executionContext;
    private final Pattern[] primaryPatterns;
    private final StringSink rowsA = new StringSink();
    private final StringSink rowsB = new StringSink();
    private final StringSink rowsC = new StringSink();
    private final String[] shadowReplacements;

    public QueryRunner(
            CairoEngine engine,
            SqlExecutionContext executionContext,
            boolean diffJit,
            boolean diffShadow,
            ObjList<FuzzTable> tables
    ) {
        this.engine = engine;
        this.executionContext = executionContext;
        this.diffJit = diffJit;
        this.diffShadow = diffShadow;
        if (diffShadow) {
            int n = tables.size();
            primaryPatterns = new Pattern[n];
            shadowReplacements = new String[n];
            for (int i = 0; i < n; i++) {
                FuzzTable t = tables.getQuick(i);
                FuzzTable shadow = t.getShadow();
                if (shadow == null) {
                    throw new IllegalArgumentException("diffShadow requires every table to have a shadow sibling: " + t.getName());
                }
                primaryPatterns[i] = Pattern.compile("\\b" + Pattern.quote(t.getName()) + "\\b");
                shadowReplacements[i] = Matcher.quoteReplacement(shadow.getName());
            }
        } else {
            primaryPatterns = null;
            shadowReplacements = null;
        }
    }

    public Result run(GeneratedQuery query) {
        String sql = query.sql();
        if (!diffJit && !diffShadow) {
            Outcome outcome = runOnce(sql, rowsA);
            return toResult(sql, outcome);
        }
        int prevJitMode = executionContext.getJitMode();
        try {
            // Pivot: primary at JIT-off. The pivot is the right-hand side of
            // the JIT comparison and the left-hand side of the storage
            // comparison, so it runs whenever either diff is on.
            executionContext.setJitMode(SqlJitMode.JIT_MODE_DISABLED);
            Outcome bJ = runOnce(sql, rowsB);

            Result jitResult = null;
            if (diffJit) {
                executionContext.setJitMode(SqlJitMode.JIT_MODE_ENABLED);
                Outcome aJ = runOnce(sql, rowsA);
                jitResult = reconcilePair(sql, aJ, bJ, rowsA, rowsB, query.deterministic(),
                        "JIT divergence", "jit on ", "jit off");
                if (jitResult.isFailed()) {
                    return jitResult;
                }
            }

            if (diffShadow) {
                executionContext.setJitMode(SqlJitMode.JIT_MODE_DISABLED);
                String shadowSql = rewriteForShadow(sql);
                Outcome bS = runOnce(shadowSql, rowsC);
                Result storageResult = reconcilePair(
                        sql,
                        bJ,
                        bS,
                        rowsB,
                        rowsC,
                        query.deterministic(),
                        "storage divergence",
                        "primary",
                        "shadow "
                );
                if (storageResult.isFailed()) {
                    return storageResult;
                }
                return storageResult;
            }
            return jitResult;
        } finally {
            executionContext.setJitMode(prevJitMode);
        }
    }

    private static boolean isAcceptedSkip(Throwable t) {
        return t instanceof SqlException || t instanceof ImplicitCastException || t instanceof NumericException;
    }

    /**
     * One side threw a known planner-sensitivity error. These are structural
     * compile-time checks (e.g. "left side of time series join doesn't have
     * ASC timestamp order") whose outcome depends on the access path the
     * planner picks, which in turn depends on index/parquet configuration.
     * Primary and shadow legitimately produce different access paths when
     * their storage settings differ, so a one-sided error here is a planner
     * choice, not a data divergence.
     */
    private static boolean isPlannerSensitivityAsymmetry(Outcome o) {
        if (!(o.failure instanceof SqlException)) {
            return false;
        }
        return o.exceptionMessage.contains("doesn't have ASC timestamp order");
    }

    private static String renderOutcome(Outcome outcome, CharSequence rows) {
        if (outcome.failure == null) {
            return "ok, " + outcome.rowsRead + " rows:\n" + rows;
        }
        return outcome.exceptionClass + ": " + outcome.exceptionMessage;
    }

    /**
     * Multiset comparison: the two row sets are equal iff they contain the same
     * lines, regardless of order. Parallel operators (GROUP BY, etc.) do not
     * guarantee a stable iteration order across runs, but neither toggle
     * affects which rows are produced -- only the per-row evaluation. So
     * order divergence is normal; element-set divergence is a real bug.
     */
    private static boolean rowsetEquals(StringSink a, StringSink b) {
        // Fast path: identical text.
        if (a.length() == b.length() && a.toString().contentEquals(b)) {
            return true;
        }
        String[] linesA = a.toString().split("\n", -1);
        String[] linesB = b.toString().split("\n", -1);
        if (linesA.length != linesB.length) {
            return false;
        }
        Arrays.sort(linesA);
        Arrays.sort(linesB);
        for (int i = 0; i < linesA.length; i++) {
            if (!linesA[i].equals(linesB[i])) {
                return false;
            }
        }
        return true;
    }

    private static Result toResult(String sql, Outcome outcome) {
        if (outcome.failure == null) {
            return Result.ok();
        }
        if (isAcceptedSkip(outcome.failure)) {
            return Result.skipped(outcome.exceptionClass + ": " + outcome.exceptionMessage);
        }
        return Result.failed(sql, outcome.failure);
    }

    private AssertionError divergence(String diffName, Outcome a, Outcome b,
                                      CharSequence rowsA, CharSequence rowsB,
                                      String labelA, String labelB) {
        String msg = diffName + ":\n"
                + "  " + labelA + ": " + renderOutcome(a, rowsA) + "\n"
                + "  " + labelB + ": " + renderOutcome(b, rowsB);
        Throwable cause = a.failure != null ? a.failure : b.failure;
        return cause == null ? new AssertionError(msg) : new AssertionError(msg, cause);
    }

    /**
     * Compares one outcome pair. Used for both the JIT axis (primary @ on vs
     * primary @ off) and the storage axis (primary vs shadow). The {@code
     * diffName} and {@code labelA}/{@code labelB} parameters only affect the
     * failure message; the comparison logic itself is identical -- both axes
     * must produce the same row multiset on a deterministic query, the same
     * row count on a non-deterministic query, and the same exception class
     * (modulo per-row message variance on non-deterministic queries) when
     * both throw.
     */
    private Result reconcilePair(String sql, Outcome a, Outcome b, StringSink rowsA, StringSink rowsB,
                                 boolean deterministic, String diffName, String labelA, String labelB) {
        // Both succeeded.
        if (a.failure == null && b.failure == null) {
            if (rowsetEquals(rowsA, rowsB)) {
                return Result.ok();
            }
            // Non-deterministic queries (LIMIT without fully-disambiguating
            // ORDER BY over parallel GROUP BY / hash join, etc.) can return a
            // different valid subset of rows on each run. Row content diff is
            // meaningless for them, but a row count diff is still a real bug:
            // the two paths must agree on how many rows survive the LIMIT,
            // regardless of which rows they are.
            if (!deterministic && a.rowsRead == b.rowsRead) {
                return Result.ok();
            }
            return Result.failed(sql, divergence(diffName, a, b, rowsA, rowsB, labelA, labelB));
        }
        // Both threw the same exception. Outcome the same way the single-run
        // path would: skip if allowlisted, fail if not.
        if (a.failure != null && b.failure != null
                && a.exceptionClass.equals(b.exceptionClass)
                && a.exceptionMessage.equals(b.exceptionMessage)) {
            if (isAcceptedSkip(a.failure)) {
                return Result.skipped(a.exceptionClass + ": " + a.exceptionMessage);
            }
            return Result.failed(sql, a.failure);
        }
        // Both threw the same allowlisted exception class but with different
        // messages. For a non-deterministic query, a per-row cast/numeric
        // exception fires on whichever row the iterator visits first, and
        // parallel GROUP BY / hash join paths can return rows in different
        // orders across runs. For a deterministic query under the storage
        // diff, the two paths can hit different first errors at compile time
        // because index/parquet settings change which predicate the planner
        // evaluates first (e.g. one side reports "STRING constant expected"
        // while the other reports a missing cast on a different sub-expr).
        // In both cases both paths rejected the query with a consistent
        // error class, so treat as skip.
        if (a.exceptionClass.equals(b.exceptionClass) && isAcceptedSkip(a.failure)) {
            return Result.skipped(a.exceptionClass + ": (varies)");
        }
        // One side threw a known planner-sensitivity error and the other
        // accepted the query. The error is a structural check that depends
        // on cursor metadata (e.g. whether the access path preserves
        // timestamp order), and that metadata can legitimately differ when
        // primary and shadow have different index/parquet configurations:
        // an indexed-symbol predicate uses an index lookup that loses TS
        // ordering, while a non-indexed scan preserves it. Skip rather than
        // flag, since the diff is a planner choice, not a data divergence.
        if (isPlannerSensitivityAsymmetry(a) || isPlannerSensitivityAsymmetry(b)) {
            return Result.skipped("planner sensitivity asymmetry");
        }
        // Anything else: divergence (one succeeded, one threw a non-skip
        // error; or both threw different exception classes).
        return Result.failed(sql, divergence(diffName, a, b, rowsA, rowsB, labelA, labelB));
    }

    private String rewriteForShadow(String sql) {
        String result = sql;
        for (int i = 0; i < primaryPatterns.length; i++) {
            result = primaryPatterns[i].matcher(result).replaceAll(shadowReplacements[i]);
        }
        return result;
    }

    private Outcome runOnce(String sql, StringSink rows) {
        rows.clear();
        try (RecordCursorFactory factory = engine.select(sql, executionContext)) {
            try (RecordCursor cursor = factory.getCursor(executionContext)) {
                RecordMetadata metadata = factory.getMetadata();
                int columnCount = metadata.getColumnCount();
                Record record = cursor.getRecord();
                int rowsRead = 0;
                while (cursor.hasNext()) {
                    for (int i = 0; i < columnCount; i++) {
                        CursorPrinter.printColumn(record, metadata, i, rows, false);
                        rows.put('\t');
                    }
                    rows.put('\n');
                    rowsRead++;
                }
                return Outcome.ok(rowsRead);
            }
        } catch (SqlException e) {
            return Outcome.error(e, e.getFlyweightMessage().toString());
        } catch (ImplicitCastException e) {
            return Outcome.error(e, e.getFlyweightMessage().toString());
        } catch (NumericException e) {
            return Outcome.error(e, "");
        } catch (Throwable t) {
            String msg = t.getMessage();
            return Outcome.error(t, msg == null ? "" : msg);
        }
    }

    private record Outcome(int rowsRead, Throwable failure, String exceptionClass, String exceptionMessage) {

        static Outcome error(Throwable t, String message) {
            return new Outcome(0, t, t.getClass().getSimpleName(), message);
        }

        static Outcome ok(int rowsRead) {
            return new Outcome(rowsRead, null, null, null);
        }
    }

    public static final class Result {
        private final Throwable failure;
        private final String skipReason;
        private final String sql;
        private final State state;

        private Result(State state, String skipReason, String sql, Throwable failure) {
            this.state = state;
            this.skipReason = skipReason;
            this.sql = sql;
            this.failure = failure;
        }

        public static Result failed(String sql, Throwable t) {
            return new Result(State.FAILED, null, sql, t);
        }

        public static Result ok() {
            return new Result(State.OK, null, null, null);
        }

        public static Result skipped(String reason) {
            return new Result(State.SKIPPED, reason, null, null);
        }

        public Throwable getFailure() {
            return failure;
        }

        public String getSkipReason() {
            return skipReason;
        }

        public String getSql() {
            return sql;
        }

        public boolean isFailed() {
            return state == State.FAILED;
        }

        public boolean isSkipped() {
            return state == State.SKIPPED;
        }

        private enum State {OK, SKIPPED, FAILED}
    }
}
